public class strings {
    public static void main(String[] args) {
        // Strings
            // A string is an object that holds a bunch of characters. For example, "Hello" is a string of 5 characters

        // String Concatenation
            // The + operator can be used to add strings together, aka concatenation. For example:
        
        String firstname, lastname;
        firstname = "John";
        lastname = "Doe";
        System.out.println("My name is: " + firstname + " " + lastname);
    }
}